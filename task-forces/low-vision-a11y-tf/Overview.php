    <p><a href="mailto:public-low-vision-a11y-tf@w3.org">public-low-vision-a11y-tf@w3.org</a> (public <a href="https://lists.w3.org/Archives/Public/public-low-vision-a11y-tf/">archive</a>)</p>
    <p>When using the <abbr title="Low vision accessibility task force">Low Vision Accessibility Task Force </abbr> mailing list:</p>
    <ol>
      <li>Treat other list members with respect.</li>
      <li>Keep comments within the scope of this group (see <a href="work-statement">Low Vision Accessibility Task Force Work Statement</a>).</li>
      <li>When raising an issue:
        <ul>
          <li>provide a clear subject line including the topic it relates          to</li>
          <li>explain the issue briefly</li>
          <li>propose a brief &amp; specific solution</li>
          <li>for longer comments, put a summary at the beginning</li>
        </ul>
      </li>
      <li>When replying:
        <ul>
          <li>keep your reply comments near the top of a message</li>
          <li>only quote essential parts of a previous message</li>
          <li>if there are multiple topics, break them into separate emails to          facilitate threading and to keep the subject line accurate</li>
        </ul>
      </li>
      <li>Please send any questions about the mailing list itself to the      Facilitators or Staff Contacts, not the whole list.</li>
      <li>No soliciting.</li>
    </ol>
    <h2 id="participation">Participation</h2>
    <p>To participate in the Low Vision Accessibility Task Force, individuals must  also be participants of the AG WG. (You don't have to have previously participated in AG WG. You do have to sign up to participate in AG WG in order to participate in LVTF).</p>
    <p>Participants are expected to actively contribute to the work of LVTF, including:</p>
    <ul>
      <li>Minimum 4 hours per week of LVTF work (this time  counts towards the individual's participation requirement in the AG WG)</li>
      <li>Complete <a href="https://www.w3.org/2002/09/wbs/81151/all">LVTF surveys</a></li>
      <li>Remain current on the <a href="https://lists.w3.org/Archives/Public/public-low-vision-a11y-tf/">LVTF mailing list</a> and respond in a timely manner to postings</li>
      <li>Participate in LVTF teleconferences, or record regrets in the <a rel="nofollow" href="https://www.w3.org/2002/09/wbs/81151/LVTF-telecons/">Availability for Upcoming LVTF Teleconferences Survey</a></li>
    </ul>
    <p>To become a participant of Low Vision Accessibility Task Force or if you have questions, contact <a href="mailto:group-lvtf-plan@w3.org?subject=Low Vision Accessibility Task Force Enquiry">the Facilitators and Team contact via mailto:group-lvtf-plan@w3.org</a>.</p>
