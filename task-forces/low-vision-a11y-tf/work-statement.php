      <h2 id="objective">Objective</h2>
      <p>The objective of the Low Vision Accessibility Task Force is to identify issues and specify solutions to web accessibility issues specific to users with low vision via WCAG 2.0 techniques, understanding and guidance documents, and as needed to produce extensions to WCAG 2.0. Since many of the low vision issues may be addressed through the browser, this work will also include references and use cases related to UAAG 2.0. This work includes:</p>
      <ul>
         <li>Document requirements for low vision users;</li>
					<li>Identify gaps in the above requirements between user needs and how they are met by web sites that conform to WCAG 2.0;</li>
					<li>Document sufficient and advisory techniques and failures for WCAG 2.0 to meet low vision user needs;</li>
					<li>Define low vision use cases which user agents need to support; </li>
					<li>Develop extension to WCAG 2.0 to address low-vision user needs effectively;</li>
         <li>Develop support materials to explain the issues and implementation of the above resources effectively.</li>
      </ul>
   </div>
   <div>
      <h2 id="approach">Approach</h2>
      <p>Initially the Task Force will refine the scope of work need to provide complete information on how to support low vision accessibility. The work will likely be broken down into modular components that can be used as independent resources or as part of a cohesive suite. The individual components of the Low Vision A11Y Task Force's work may be developed as W3C Recommendations, W3C Working Group Notes, or other W3C / WAI resources. This will be decided after the development of an initial, more detailed set of requirements for handling accessibility for low vision users.</p>
      <p>The work will be carried out iteratively with continual involvement of the public throughout the development. In particular, key stakeholders such as developers, evaluators, experts, researchers, and users will be regularly involved in the development process of the work of the Low Vision Accessibility Task Force.</p>
   </div>
   <div>
     <h2 id="dependencies">Dependencies</h2>
      <p>The Low Vision Accessibility Task Force depends on the <a href="http://www.w3.org/WAI/GL/">AG WG</a> to ensure accurate interpretation and representation of WCAG requirements.</p>
      <p>The Low Vision Accessibility Task Force will also coordinate with the <a href="http://www.w3.org/WAI/APA/">Accessible Platform Architectures Working Group (APA WG)</a>, for technical advice and <a href="http://www.w3.org/WAI/EO/">Education and Outreach Working Group (EOWG)</a> where necessary to improve the writing, presentation, and educational value of the developed materials.</p>
   </div>
