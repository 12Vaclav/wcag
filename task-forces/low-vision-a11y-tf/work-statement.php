      <h2 id="participation">Participation</h2>
      <p>To join the Low Vision A11Y TF, individuals must be participants of the AG WG. Participants are expected to actively contribute to the work of Low Vision A11Y TF, including:</p>
      <ul>
         <li>Minimum 4 hours per week of Low Vision A11Y TF work (this time also counts towards the individual's participation requirement in the sponsoring WG through which they have joined);</li>
         <li>Remain current on the Low Vision Accessibility Task Force mailing list and respond in a timely manner to postings;</li>
         <li>Participate in Low Vision Accessibility Task Force telephone meetings, or send regrets to the Low Vision Accessibility Task Force mailing list.</li>
      </ul>
