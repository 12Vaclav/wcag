				<h3>Issues</h3>
				<p>Our process for issues, which has gone though TF consensus is:</p>
				<ol>
					<li>Take an issue (assign it to you)</li>
					<li>Make a proposal. (currently we have a google doc for proposals - see our action/todo list)</li>
					<li>Inform  the list</li>
					<li>Once we have four +1 and no objections you can answer the issue and close it</li>
				</ol>
				<p class="note">Note: comments on opened issues should be from the task force. Please do not respond without going through the process</p>
