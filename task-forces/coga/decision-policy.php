		<section>
			<h2>Decisions</h2>
			<p>The Cognitive and Learning Disabilities Accessibility Task Force (COGA) strives to reach consensus via unanimous agreement. </p>
			<section>
				<h3>Small items</h3>
				<p>such as wording changes or normal issue responses that will be part of a final CFC later has the following process:</p>
				<ul>
					<li>Agreement on the email list <strong>or</strong></li>
					<li>Agreement on the call</li>
				</ul>
			</section>
			<section>
				<h3>Medium items</h3>
				<p>such as important issue responses (such as an issue from  the tag, or other working group)</p>
				<ul>
					<li>Agreement on the email list <strong>and</strong></li>
					<li>Agreement on the call</li>
					<li>All decisions are recorded on a wiki page with date and link</li>
				</ul>
			</section>
			<section>
				<h3>Large items</h3>
				<p>such as asking the parent group to open a cfc publication</p>
				<ul>
					<li>Agreement on the call <strong>and</strong></li>
					<li>Agreement on the email list with at least 2 working days <strong>and</strong></li>
					<li>Clear wording on the email to the list with attention grabbing subject line and clear instructions and deadlines such as calling it a CFC and using that template</li>
					<li>All decisions are recorded on a wiki page with date and link</li>
				</ul>
			</section>
		</section>
		<section>
			<h2>Discussion</h2>
			<ul>
				<li>Facilitators encourage discussion to continue until all points of view have been expressed and the group has considered the variety of information presented.</li>
				<li>Facilitators encourage all participants  to express their views.</li>
				<li>Discussion may include outreach outside the call (emails, surveys,etc)</li>
				<li>COGA strives to use technology that people who want to participate can use. This is especially important for decisions.</li>
				<li>COGA strives to have a review by a representative of each disability group or minority affected by important  decisions.</li>
				<li>During discussion, participants can raise objections freely. </li>
			</ul>
		</section>
		<section>
			<h2>Agreement</h2>
			<ul>
				<li>COGA strives to get a quorum for responses. Quorum depends on the context.  <ul>
						<li>Typically, at least in a call there should be at least 4 non-leadership members present who  agree. </li>
						<li>If Facilitators say that a lack of response will be considered agreement, less people have to agree to reach quorum. (But they should have had the opportunity to respond)</li>
					</ul></li>
				<li>If objections to a proposed agreement are raised, the facilitators should try and find an alternative that everyone can live with</li>
				<li>COGA strives to provide enough time for participants to consider before making a decision.</li>
				<li>When finalizing a decision facilitators can give a time limit and time table for objections. </li>
				<li>When discussion is done and the group is making a decision, participants should only raise objections that they “can’t live with.” <ul>
						<li>Compromise on points people can "live with" is an essential part of decisions. </li>
						<li>The facilitators can allow a decision to go through with some objections if a significant majority approves the decision. </li>
					</ul></li>
				<li>Decisions should ideally be based on research-supported recommendations.</li>
			</ul>
		</section>
