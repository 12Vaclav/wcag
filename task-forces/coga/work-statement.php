				<div>
					<h2 id="objective">Objective</h2> 
					<p>The objective of the Cognitive and Learning Disabilities Accessibility Task Force is to improve accessibility of web content, applications, and digital content for people with cognitive disabilities and people with learning disabilities.</p>
					<p><strong>This includes:</strong></p>
					<ul>
						<li>cognitive disabilities,</li>
						<li>learning disabilities (LD),</li>
						<li>neurodiversity,</li>
						<li>intellectual disabilities,</li>
						<li>age-related memory loss,</li>
						<li>a subset of mental health conditions that have clear research that can be integrated into Making Content Usable,</li>
						<li>and specific learning disabilities (SLD). [<a href="#References">Making Content Usable 2.4, Language Use</a>]</li>
					</ul>
					<p><strong>Our work includes:</strong></p>
					<ul>
						<li>research and analysis,</li>
						<li>developing guidance and techniques to make web content, content authoring, and user agents accessible and more usable by people with cognitive and learning disabilities, and</li>
						<li>review existing techniques and consider ways to improve them, and build new techniques where necessary.</li>
					</ul>
					<p>We will consider mental health conditions and how we can best integrate them into our work.</p>
				
				</div>
				<div>
				<h2 id="approach">Approach</h2>
				<p>The work of the COGA TF includes:</p> 
				<ul>
						<li>Performing research and analysis with the intent of publishing;</li>
						<li>Creating guidance for Web content authors;</li>
						<li>Creating guidance for creators of applications and platforms;</li>
						<li>Co-creating guidelines with the Silver TF for WCAG 3.0;</li>
						<li>Helping other working groups in the W3C have more inclusive deliverables;</li>
						<li>Identifying related work inside and outside of the W3C;</li>
						<li>Reviewing existing techniques and propose new features where needed;</li>
						<li>Gathering information on different related technologies and techniques including where techniques in support of users with cognitive and learning disabilities may conflict with what is needed for other users;</li>
						<li>Building a use case repository to describe different cognitive disabilities and scenarios for how persons with atypical ability profiles use technology successfully and how failures may occur;</li>
						<li>Making predictions of how emerging technologies might be used to improve (or even potentially hinder) cognitive accessibility;</li>
						<li>Documenting business cases and identify areas of resistance, risks and opportunities.</li>
					</ul>
			
				<p>The Task Force expects to produce quantitatively testable authoring techniques, but also expects to produce techniques that are not testable by automated checkers and must involve user testing. Tests and feedback will help ensure techniques give real benefits to end users and can be used by authors, tool providers, browsers, and assistive technologies.</p>
				<p>The Task Force plans to produce the following deliverables:</p>
				<ul>
						<li>Making Content Usable (second draft),</li>
						<li>Research module/ literary review and/or issues papers,</li>
						<li>Co-created guidelines with the Silver TF for WCAG 3.0.</li>
					
					</ul>

					<p>The Task Force may form subteams to carry out work of the Task Force. See our wiki <a href="https://www.w3.org/WAI/GL/task-forces/coga/wiki/Subgroups">Subgroups page</a> for more details, including subteam work statements.</p>
