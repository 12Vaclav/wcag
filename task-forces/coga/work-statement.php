					<h2 id="Background">Background</h2> 
					<p> Our work includes <a href="https://www.w3.org/TR/coga-usable">Making Content Usable for People with Cognitive and Learning Disabilities</a>. This document is for people who are involved in making web content, applications, and digital content. </p>
					<p>The Task Force (TF) is currently recruiting people to help with:</p>
					<ul>
						<li>literary reviews and research relating to web accessibility for mental health,</li>
						<li>creating cognitively accessible deliverables, both text, audio, images, and other multimedia, and</li>
						<li>understanding our user groups (people with learning and cognitive disabilities and mental health related disabilities).</li>
					</ul>
					<p>More details about our work, and how to work with us, are available on our <a href="https://www.w3.org/WAI/GL/task-forces/coga/wiki/Main_Page">COGA wiki</a>.</p>
				
				
