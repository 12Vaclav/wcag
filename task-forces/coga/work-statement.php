					<h2 id="status">Status</h2> <p> This is a Work Statement for the Cognitive A11Y TF. It has been approved by the AG WG and the APA WG. Recruiting is beginning and the Task Force will soon be active. </p></div>
				<div>
					<h2 id="objective">Objective</h2> <p>The objective of the Cognitive and Learning Disabilities Accessibility Task Force is to improve Web accessibility for people with cognitive and learning disabilities. This will begin with research and gap analysis. Then the group will develop draft proposed guidance and techniques to make web content, content authoring, and user agent implementation accessible and more usable by people with cognitive and learning disabilities. It will also review existing techniques and consider ways to improve them, and build new techniques where necessary.</p></div>
				<div>
				<h2 id="approach">Approach</h2><p>The Cognitive A11Y TF will:</p> <ul>
		<li>Identify related work inside and outside of the W3C;</li>
					<li>Review existing techniques and propose new features where needed;</li>
		<li>Gather information on different related technologies and techniques including where techniques in support of cognitive and learning disabled users may conflict with what is needed for other users;</li>
		<li>Build a repository of related research into cognition and disabilities;</li>
		<li>Build aÂ  use case repository to describe different cognitive disabilities and scenarios for how persons with atypical ability profiles use technology successfully or unsuccessfully;</li>
		<li>Make predictions of where technology is headed;</li>
		<li>Identify stakeholders, from whom the task force can solicit ongoing feedback;</li>
		<li>Document business cases and identify areas of resistance, risks and opportunities. </li>
		</ul><p>The task force expects to produce some testable authoring techniques, but also expects to produce techniques that are not testable. Tests and feedback will help ensure techniques give real benefits to end users and can be used by authors, tool providers, browsers, and assistive technologies. </p><p>The task force plans to produce the following deliverables:</p> <ul>
		<li>Gap analysis;</li>
		<li>Roadmap document;</li>
		<li>Techniques for Web content authors. </li>
		</ul>
				<p>The Task Force may form subteams to carry out work of the Task Force.</p></div>
