				<div><h2 id="dependencies">Dependencies</h2>

					<p>The Cognitive and Learning Disabilities Accessibility Task Force has the following dependencies:</p>
					<ul>
						<li><a href="http://www.w3.org/WAI/GL/">AGWG</a> and <a href="http://www.w3.org/WAI/APA/">APA WG</a> to utilize this group's previous and current work that addresses the cognitive accessibility space;</li>
						<li><a href="http://www.w3.org/WAI/GL/">AG WG</a> to ensure accurate interpretation and representation of WCAG requirements;</li>
						<li> <a href="http://www.w3.org/WAI/GL/wcag-pf-html5-task-force">HTML5 and ARIA Techniques Task Force</a> to leverage the development of HTML 5 and ARIA techniques for cognitive accessibility.</li>
					</ul>
					<p>The Cognitive and Learning Disabilities Accessibility Task Force will also coordinate with the <a href="http://www.w3.org/WAI/EO/">Education and Outreach Working Group (EOWG)</a> where necessary to improve the writing, presentation, and educational value of the developed materials.</p>
