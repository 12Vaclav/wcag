		<section id="participation">
			<h2>Participation</h2>
			<p>To join the Silver Task Force, individuals must be participants of Accessibility Guidelines WG. Participants are expected to actively contribute to the work of the task force, including: </p>
			<ul>
				<li>Substantial and consistent task force work (this time also counts towards the individual's participation requirement in the AG WG);</li>
				<li>Remain current on the task force mailing list and respond in a timely manner to postings;</li>
				<li>Participate regularly in task force telephone meetings, or send regrets ahead of time.</li>
			</ul>
			<p>This task force has two recognized levels of participation: a core contributing eight hours or more per week, and a larger group contributing two to four hours per week on a consistent basis.</p>
			<p>If you are interested in becoming a participant of the Silver Task Force or have any questions regarding its work, contact the task force facilitators. </p>
