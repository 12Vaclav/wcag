		<h1>Accessibility Guidelines "Silver" Task Force Work Statement</h1>
		<p>The Silver Task Force is a task force of the <a href="https://www.w3.org/WAI/GL/">Accessibility Guidelines Working Group (AG WG)</a>. It assists this Working Group with the work identified below.</p>
		<section id="status">
			<h2>Status</h2>
			<p>The Silver Task Force was <a href="https://lists.w3.org/Archives/Public/w3c-wai-gl/2016OctDec/0396.html">approved on 4 November 2016</a> and is now active. The <a href=".">Task Force home page</a> contains information about the operation and resources of the group.</p>
		</section>
		<section id="objective">
			<h2>Objective</h2>
			<p>The objective of the task force is to perform preliminary development of a new version of Accessibility Guidelines following a research-focused, user-centered design methodology to produce the most effective and flexible outcome. Code-named "Silver", these guidelines will address the process of making content and functionality accessible to people with disabilities, including the roles of content authoring, user agent support, and authoring tool support. These guidelines will address current technological and cultural web accessibility requirements and provide a base for continued evolution of the guidelines.</p>
		</section>
		<section id="scope">
			<h2>Scope of Work</h2>
			<p>The work of the task force includes: </p>
			<ul>
				<li>Propose options of plans to update the Accessibility Guidelines WG and choose an option;</li>
				<li>Identify stakeholders;</li>
				<li>Design research studies and timelines to perform research about appropriate structure and features of future guidelines;</li>
				<li>Perform research and report on findings;</li>
				<li>Interpret, analyze, and report on research findings;</li>
				<li>Create and refine prototypes and choose an option;</li>
				<li>Develop Requirements for Silver;</li>
				<li>Write an initial editorâ€™s draft of Silver in coordination with the Accessibility Guidelines Working Group.</li>
			</ul>
		</section>
		<section id="approach">
			<h2>Approach</h2>
			<p>The Silver Task Force is intended to be an intensive focused group to develop a solid proposal for the structure and content of Silver. In order to have a more flexible result, the task force will incorporate a diverse set of perspectives throughout the process. Input will be collected via surveys, requests for input, polls, etc. and sent via diverse channels (i.e., mailing lists, organizations, meetups, etc.) in order to include those who have valuable perspectives but may not have the time or resources to more directly take part in the development of the project. To enable this, participants in the task force are expected to devote substantial amounts of time, forming a small but active group.</p>
			<p>The task force will coordinate regularly with the Accessibility Guidelines Working Group to keep the larger base of participants informed and receive guidance from the WG.</p>
		</section>
		<section id="liaisons">
			<h2>Liaisons</h2>
			<p>The Silver Task Force depends on the <a href="https://www.w3.org/WAI/GL/">Accessibility Guidelines WG</a> to ensure accurate interpretation and representation of AG requirements;</p>
			<p>The task force will coordinate with the <a href="https://www.w3.org/WAI/EO/">Education and Outreach Working Group (EOWG)</a> where necessary to improve communication with the public, and improve the writing, presentation, and educational value of developed materials.</p>
			<p>The task force will coordinate with the <a href="https://www.w3.org/WAI/APA/task-forces/research-questions/">Research Questions Task Force</a> for advice on research methodologies, input from relevant research coordinated by the RQTF, and to avoid overlaps in scope.</p>
			<p>The task force will coordinate with the <a href="https://www.w3.org/WAI/APA/">APA Working Group</a> for input from its specification review and user requirements work and their implications for upcoming W3C technologies.</p>
		</section>
