		<h1>AG Task Forces</h1>

		<p>The Accessibility Guidelines Working Group conducts some of its work via the following task forces. See each task force page for details about specific work and how to join.</p>
		<p>Task force facilitators <a href="https://www.w3.org/2017/08/telecon-info_ag-facilitators">meet the first Wednesday of the month</a> for coordination (also see <a href="https://www.w3.org/WAI/GL/WCAG3/minutes/?channel_id=ag-facilitators">AG facilitators minutes</a>). Task force coordination is maintained in the <a href="https://www.w3.org/WAI/GL/wiki/AG_Facilitators">AG Facilitators wiki</a>.</p>

		<section id="act">
			<h2>Accessibility Conformance Testing (ACT) Task Force</h2>
			<p>The <a href="conformance-testing/">Accessibility Conformance Testing Task Force</a> will develop a framework and repository of test rules, to promote a unified interpretation of WCAG 2.0 among different web accessibility test tools. The framework will also allow the development of custom test rules to meet company requirements and best practices. While this framework will add a layer of implemention for tool developers, it provides more consistency and comparability of the resulting test rules.</p>
		</section>

		<section id="coga">
		<h2>Cognitive and Learning Disabilities Accessibility Task Force</h2>
		<p>The <a href="coga/">Cognitive and Learning Disabilities Accessibility Task Force</a> will develop draft proposed guidance and techniques to make web content, content authoring, and user agent implementation accessible and more useable by people with cognitive and learning disabilities. </p>
		</section>

		<section id="lvtf">
		<h2>Low Vision Accessibility Task Force</h2>
		<p>The <a href="low-vision-a11y-tf/">Low Vision Accessibility Task Force</a> will  identify issues and specify solutions to web accessibility issues specific to users with low vision via WCAG 2.0 techniques, understanding and guidance documents, and as needed to produce extensions to WCAG 2.0.</p>
		</section>

		<section id="mobile">
		<h2>Mobile Accessibility Task Force</h2>
		<p>The <a href="/WAI/GL/mobile-a11y-tf/">Mobile Accessibility Task Force</a> produce techniques, understanding and guidance documents as well as updates to existing related W3C / WAI material that addresses the mobile space.</p>
		</section>

		<section id="silver">
		<h2>Silver Task Force</h2>
			<p>The <a href="silver/">Silver Task Force</a> will perform preliminary development of a new version of Accessibility Guidelines following a research-focused, user-centered design methodology to produce the most effective and flexible outcome. These guidelines will address the process of making content and functionality accessible to people with disabilities, including the roles of content authoring, user agent support, and authoring tool support. These guidelines will address current technological and cultural web accessibility requirements and provide a base for continued evolution of the guidelines. Also see the related <a href="https://www.w3.org/community/silver/">Silver Community Group</a>.</p>
		</section>

