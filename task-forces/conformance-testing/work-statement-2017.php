		<h1>Accessibility Conformance Testing (ACT) Task Force Work Statement (2017)</h1>
		<p>The <a href=".">Accessibility Conformance Testing Task Force (ACT TF)</a> is a task force of the <a href="https://www.w3.org/WAI/GL/">Accessibility Guidelines Working Group (AG WG)</a>. It assists this Working Group with the work identified below.</p>
		<section id="status">
			<h2>Status</h2>
			<p>This Work Statement was <a href="https://lists.w3.org/Archives/Public/w3c-wai-gl/2016JulSep/0550.html">approved on 11 August 2016</a> and was active until 23 January 2020. The <strong><a href="work-statement">latest work statement</a></strong> is available. The <a href=".">Task Force home page</a> contains information about the operation and resources of this group. </p>
		</section>
		<section id="objective">
			<h2>Objective</h2>
			<p>The objective of the Accessibility Conformance Testing (ACT) Task Force is to develop a framework and repository of test rules, to promote a unified interpretation of <a href="https://www.w3.org/WAI/intro/wcag">WCAG 2.0</a> among different web accessibility test tools. The proposed framework will be generic enough to meet the needs of WCAG 2.0 but not exclude other standards, including future versions of WCAG. The framework will also allow the development of custom test rules to meet company requirements and best practices. This includes test rules for fully automated and semi-automated conformance testing in different web technologies, such as HTML, CSS, and WAI-ARIA. While this framework will add a layer of implemention for tool developers, it provides more consistency and comparability of the resulting test rules. </p>
		</section>
		<section id="scope">
			<h2>Scope of Work</h2>
			<p>The work of the task force includes: </p>
			<ul>
				<li><strong>ACT Framework:</strong> Document specifying the format, logic, and implementation of the test rules. This document is expected to become a W3C Recommendation;</li>
				<li><strong>ACT Benchmark Method:</strong> Document specifying the procedure for validating test rules. This may be a W3C Note or part of the ACT Framework document (above);</li>
				<li><strong>ACT Benchmark Tool:</strong> Software built according to the ACT Framework and ACT Benchmark Method, to help check the validity of test rules to these documents;</li>
				<li><strong>ACT Rule Suite Repository:</strong> Authoritative set of approved test rules are developed by auto-WCAG or submitted to W3C in accordance with the <a href="https://www.w3.org/Consortium/Patent-Policy/">W3C Patent Policy</a>;</li>
				<li><strong>ACT Rule Suite Frontend:</strong> Web-based user interface (application) for submitting, validating, versioning, and presenting a collection test rules to the public.</li>
			</ul>
			<p><a href="https://www.w3.org/WAI/GL/task-forces/conformance-testing/wiki/ACT_Deliverables">Further description of these deliverables</a> is provided in the ACT TF wiki.</p>
		</section>
		<section id="approach">
			<h2>Approach</h2>
			<p>This work will be largely based on the <a href="https://www.w3.org/community/auto-wcag/wiki/">resources already developed by Auto-WCAG</a>. Only the ACT Framework and ACT Benchmark Method documents are expected to be developed directly within this Task Force. The remaining deliverables are expected to continue being developed within Auto-WCAG, with oversight by the Task Force. This is to maximize participation and leverage contribution by the broader community, while ensuring close coordination and harmonization with the Web Content Accessibility Guidelines (WCAG).</p>
			<p>The deliverables to be developed within Auto-WCAG are expected to become the reference implementations for the development of the ACT Specification as a W3C Recommendation. The initial focus of this work will be on automated testing for WCAG 2.0 Level AA using HTML, CSS, and ARIA.</p>
		</section>
