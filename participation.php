<section>  
  <h2 id="Becoming">Become a participant</h2>

<p>Participation enables you to influence the development of the deliverables, and you and your organization will be listed as contributors to the deliverables of the AG WG where appropriate. Participant status, however, requires a commitment on your behalf to support the work of the group. Participation in <a href="taskforces/">task forces</a> counts towards overall participation expectations for the Working Group as a whole.</p>
  
  <p>Participants should:</p>
  <ul>
    <li>Engage actively with the Working Group via any of the communication mechansims available;</li>
    <li>Provide input in a timely fashion.</li>
    <li>Maintain familiarity with the progress of the Working Group via minutes of past meetings, mailing list discussion, issue comments, etc.</li>
  </ul>
			<p>Participating in the Working Group requires a W3C account; you can <a href="https://www.w3.org/accounts/request">create an account</a> if you do not already have one. Before applying to join, please read the <a href="charter">AG WG Charter</a> to review the activity and commitments. When you join the group, you are automatically added to the <a href="contribute#lists">mailing lists</a> and provide access to the WG <a href="communication">communication tools</a>.</p>
			<p><a href="https://www.w3.org/2000/09/dbwg/details?group=35422&amp;public=1">Current participants in the AG WG</a>.</p>
			<section>
				<h2>Employees of Member organizations</h2>
				<p>Employees of Member organizations automatically have the right to participate. To join, ask your <a href="/Member/ACList">W3C Advisory Committee (AC) Representative</a> to <a href="http://www.w3.org/2004/01/pp-impl/35422/join">nominate you to the Working Group</a>. You can also use available public contribution channels, above, without joining the group if you or your employer prefer.</p>
			</section>
			<section>
				<h2>Individuals</h2>
				<p>If you do <strong>not</strong> work for a <a href="http://www.w3.org/Consortium/Member/List">W3C Member organization</a>, but you feel you have the expertise and availability to participate, you should first use public contribution channels, above, to familiarize yourself with the work and make yourself known to the group. If your contribution suggests a higher level of participation is warranted, you can be invited to join the group as an Invited Expert. Please <a href="mailto:group-ag-chairs@w3.org">contact the WG chairs</a> to discuss this. If you work for an organization whose business is related to that of the W3C, we may instead ask your organization to join and nominate you as a Member representative.</p>
				<p>If the chairs support it, you may complete the <a href="https://www.w3.org/ieapp/new">invited expert application</a> form, indicating the Accessibility Guidelines WG as the group you wish to join. Make sure you have read and accepted the policies referenced from that form, and include details about your relevant expertise, interest in the work, and your employer. <em>If you have not made yourself known to the chairs, or your application does not provide information about your expertise and expected contribution, it may be rejected.</em>
				</p>
