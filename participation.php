<p>This page explains the requirements and procedures for becoming a
participant in the AG WG. It also explains how non-participants may
contribute to the Working Group by subscribing to the group's mailing list,
monitoring the group's progress, and contributing on that list. See also <a href="http://www.w3.org/WAI/participation">Participating in WAI</a> for broader participation options throughout the Web Accessibility Initiative (WAI).</p>

